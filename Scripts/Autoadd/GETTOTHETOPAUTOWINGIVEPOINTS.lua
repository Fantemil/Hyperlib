local lib = loadstring(game:HttpGet("https://raw.githubusercontent.com/xHeptc/Kavo-UI-Library/main/source.lua"))()
local window = lib.CreateLib("Get to the top", "DarkTheme")
local tab = window:NewTab("Main")
local section = tab:NewSection("Main")
local p = game:GetService("Players")
local chr = p.LocalPlayer.Character
local hum = chr.HumanoidRootPart

section:NewLabel("Point multiplier will cause small lag when winning")
section:NewButton("TP to top", "i", function()
    hum.CFrame = CFrame.new(14.403511, 625.5, -540.754272)
    wait(0.1)
    hum.CFrame = CFrame.new(14.403511, 795.5, -540.754272)
    wait(0.1)
    hum.CFrame = CFrame.new(14.403511, 965.5, -540.754272)
    wait(0.1)
    hum.CFrame = CFrame.new(14.403511, 1135.5, -540.754272)
end)
section:NewButton("Enable point multiplier (x6)", "i", function()
    p.LocalPlayer.PlayerGui.ChildAdded:connect(function(rewards)
        if rewards:IsA("ScreenGui") and rewards.Name == "RewardGui2" then
            rewards.go.PointsSend:FireServer()
            rewards.go.PointsSend:FireServer()
            rewards.go.PointsSend:FireServer()
            rewards.go.PointsSend:FireServer()
            rewards.go.PointsSend:FireServer()
            rewards.go.PointsSend:FireServer()
        end
    end)
end)