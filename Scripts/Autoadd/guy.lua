	object = { -- looks nice and easy tot expand
		Find = function(Object, str, Property)
			str = string.lower(str) -- awalays recommend for User searchs
			for i, v in ipairs(Object:GetChildren()) do -- ipars check if there are nil instances and skips(slow but less bugs)
				if Property and string.find(string.lower(v[Property]), str) then -- object[prob] accesses propertry
					return v
				end
				if string.find(string.lower(v.Name), str) then -- so Name Servhes work as well
					return v
				end
			end
		end
	}

	local function Char(player)
		if not player then
			if Player.Character then
				return Player.Character -- Found char!
			else
				task.wait(.1)
				Char() -- diddent find but if it will respawn if will eventually return and finish the teleport
				return -- spot it otherwise ERROR
			end
		end

		local Return = object.Find(Players, player, 'DisplayName') -- for Name and display
		if Return and not Return.Character then
			task.wait(.1)
			Char(player) -- 
			return
		end
		if Return then
			return Return.Character
		end
	end

